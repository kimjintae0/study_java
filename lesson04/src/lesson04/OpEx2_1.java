package lesson04;

public class OpEx2_1 {
	public static void main(String[] args) {
		
		int a = 5;
		
		System.out.println("+a = " + +a); // 원래 부호 그대로 표현한거라 크게 의미가 없음
		System.out.println("-a = " +- a); // 부호 부정 / 원래 양수에서 - 로 출력

		a = -5;
		
		System.out.println("+a = " + +a);
		System.out.println("-a = " + -a);
		
	}
}
