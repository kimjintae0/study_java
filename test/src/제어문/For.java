package 제어문;

public class For {
	public static void main(String[] args) {
		
		for (int i = 0; i < 5 ; i++) {
			System.out.println("안녕?");
		}
			// ★ for 문 실행 시 
//		1.선언 : 정수형 변수 i를 선언 및 0으로 초기화 
//		2.조건 : i가 5보다 작다는 조건을 만족하는 동안 반복문 내 명령문 실행 
//		3.증감 : i의 값을 1 증가
		
//		for : 반복문
//
//		한가지
//		for (선언; 조건; 증감)
//			명령문
//
//			여러개
//		for (선언; 조건; 증감)
//			명령문1
//			명령문2
//			...
//		}
	}
}

