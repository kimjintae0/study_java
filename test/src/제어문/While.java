package 제어문;

public class While {
	public static void main(String[] args) {
		
		int i = 0;
		while (i < 5) {
			System.out.println("안녕?");
			i++;
		}		
//			while 실행 시
//		1.조건 : i가 5보다 작다는 조건을 만족하는 동안 반복문 내 명령문 실행
//		2.선언 : 정수형 변수 i 를 선언 및 0 으로 초기화 > for문과 다르게 선언이 필요 블럭 밖에다가
//		3.증감 : i의 값을 1 증가

		//			while : 또 다른 반복문
//
//			한가지
//
//			while (조건)
//				명령문
//				
//			여러개
//
//			while (조건) {
//				명령문1
//				명령문2
//				...
//				}
		
	}
}


