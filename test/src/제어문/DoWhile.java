package 제어문;

import java.util.Scanner;

public class DoWhile {
	public static void main(String[] args) {
		
		int i = 0 ;
		do {
			System.out.println("안녕?");
			i++;
		} while ( i < 5 );
		
		
		// 처음 실행문은 한번 실행되고 조건식을 확인해서 true이면 반복 아니면 끝
		
//		Do while : 또 또 다른 반복문 
//
//		한가지
//
//		do
//			명령문
//		while (조건);
//
//		여러개
//
//		do {
//			명령문1
//			명령문2
//			...
//		} while (조건);
		
	}
}




